#pragma once
typedef unsigned long BSTATUS;
#define NT_SUCCESS(Status) (((NTSTATUS)(Status)) >= 0)
#define BS_SUCCESS	NT_SUCCESS

#define BSTATUS_SUCCESS				(BSTATUS)0x00000000		//成功

#define BSTATUS_ACCESS_DENIED		(BSTATUS)0xC0000000		//拒绝访问

#define BSTATUS_NOT_INITED			(BSTATUS)0xC0000001		//dll未初始化

#define BSTATUS_INVALID_PARAMETER	(BSTATUS)0xC0000002		//参数错误

#define BSTATUS_ACCESS_VIOLATION	(BSTATUS)0xC0000003		//访问违规

#define BSTATUS_INVALID_USER_NAME	(BSTATUS)0xC0000004		//用户名不存在

#define BSTATUS_UNSUCCESSFUL		(BSTATUS)0xC0000005		//失败, GetLastError()以获取win32错误

#define BSTATUS_INVALID_HANDLE		(BSTATUS)0xC0000006		//无效句柄

#define BSTATUS_BUFFER_TOO_SMALL	(BSTATUS)0xC0000007		//缓冲区太小

#define BSTATUS_INVALID_SID			(BSTATUS)0xC0000008		//sid无效

#define BSTATUS_NO_SUCH_USER		(BSTATUS)0xC0000009		//没有这个用户
